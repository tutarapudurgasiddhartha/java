public class Operators{
public static void main(String[] args)
{
int a=2,b=3,c=4;
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a*b);
System.out.println(a/b);
System.out.println(a%b);
a+=1;
b-=10;
c%=20;
System.out.println(a);
System.out.println(b);
System.out.println(c);
System.out.println(a>b);
System.out.println(b<c);
System.out.println(a==b);
System.out.println(b!=c);
System.out.println(a<b&&b>c);
System.out.println(a<b||b>c);
System.out.println(a<b&b>c);
System.out.println(a<b|b>c);
}
}     
